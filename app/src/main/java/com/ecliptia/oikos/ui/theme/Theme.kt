@Composable
fun OikosTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    financialStatus: FinancialStatus = FinancialStatus.NEUTRAL,
    dynamicColor: Boolean = false, // Disabled dynamic color to enforce the app's branding
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        else -> when (financialStatus) {
            FinancialStatus.NEUTRAL -> if (darkTheme) DarkColorScheme else LightColorScheme
            FinancialStatus.GOOD -> if (darkTheme) BlueDarkColorScheme else BlueLightColorScheme
            FinancialStatus.BAD -> if (darkTheme) RedDarkColorScheme else RedLightColorScheme
            FinancialStatus.WARNING -> if (darkTheme) OrangeDarkColorScheme else OrangeLightColorScheme
            FinancialStatus.EXCELLENT -> if (darkTheme) GoldDarkColorScheme else GoldLightColorScheme
        }
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            window.navigationBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
